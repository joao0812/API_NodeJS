import PositionModel from "../models/PositionModel.js"

//--------------------------------POST---------------------------------------
const addPosition = async (req, res) => {
    let position = new PositionModel(req.body)
    try {
        let doc = await position.save()
        console.log(`Positon added \n ${doc}`)
        res.send(`Positon added \n ${doc}`)
    }
    catch(err) {
        console.log(err)
        res.send(err)
    }
}

//--------------------------------GET---------------------------------------
const getOne = async (req, res) => {
    try {
        let doc = await PositionModel.findOne({ _id: req.params.id })

        if (doc.length === 0) {
            res.send('Notting found')
        }
        else {
            res.send(doc)
            console.log(doc._id.toString())
        }
    }
    catch (err) {
        res.send(err)
    }
}
const getAll = async (req, res) => {
    try {
        let docs = await PositionModel.find()
        docs.length === 0 ? res.send('Notting found') : res.send(docs)
    }
    catch (err) {
        res.send(err)
    }
}

//--------------------------------PUT---------------------------------------
const updateOne = async (req, res) => {
    let new_position = req.body
    try {
        let doc = await PositionModel.findOneAndUpdate({_id: req.params.id}, {$set: new_position}, {new: true, upsert: true})
    }
    catch (err) {
        console.log(err)
        res.send(err)
    }
}

//--------------------------------DELETE-------------------------------------
const removeOne = async (req, res) => {
    let id = req.params.id
    try {
        let doc = await PositionModel.findOneAndDelete({_id: id})
        doc ? console.log(`Area ${id} removed`) : console.log(doc)
    }
    catch (err) {
        console.log(err)
        res.send(err)
    }
}

export default { getOne, getAll, addPosition, updateOne, removeOne }
